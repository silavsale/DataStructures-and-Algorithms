// Remainder (%)
// The remainder operator (%) returns the remainder left over when one operand is divided by a second operand. It always takes the sign of the dividend.

console.log(10 % 3) // = 1 ; because 3 * 3 gets you 9, and 10 - 9 is 1.

console.log(12 % 5)
// expected output: 2

console.log(-12 % 5)
// expected output: -2

console.log(4 % 2)
// expected output: 0

console.log(-4 % 2)
// expected output: -0
